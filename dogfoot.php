        <p><strong>Dry food.</strong> Dry food is both convenient and typically inexpensive, with over $8 billion worth being sold in 2010 – a 50% increase over just seven years earlier. Dry food processing is popular in the pet food industry as it is an efficient way to supply continuous production of feed in various variety as it is energy efficient, allows for large amounts of feed to be used, and is cost effective. To make dog kibble, a process known as extrusion is done.

        A simple extruder consists of a barrel, helical screws, and a die (tool to cut and shape food). Some extruders in the United States also contain a fat distributor, where the food mixture enters the barrel of the extruder through a funnel-like structure. Fat is stored in a container where it is pumped through a heat exchanger and fed through a line connected to the barrel of the extruder with a valve to prevent backflow of fat. A metering device connected to this line allows for control of fat amounts deposited in the mixture. Pressure is applied by helical screws that is driven by a motor. The food mixture is shaped by going through the die when the extrusion process ends, allowing a fly knife to cut the extruded feed.</p>
