        <p>After ingredients are combined, they are placed in a tank at the end of a canning machine.[55] From there, the mixture is forced through an opening and onto a metal sheet, forming a thickness of 8 to 12mm. Next, the mixture is heated to thoroughly cook the ingredients. Heating can be done through the means of ovens, microwaves or steam heating. The sheet containing a layer of feed is passed through the heat source that displays heat to the top and bottom of the tray, allowing the internal temperature to reach 77 degrees Celsius at a minimum. Once cooked, this mixture can be directly placed into cans to form a loaf or it can be cut into “meaty” pieces for chunks and gravy formulas.</p>
